package Class1.OOPS_Concept;

public class OOPS_Theory {
    public static void main(String[] args) {
        /**
         * OOPS Concept:
         *
         * 1. Encapsulation - wrapping up the code in a class.
         *  By creating Class and writing code in it, we practice Encapsulation.
         *
         * 2. Abstraction - Hiding unnecessary information from the user.
         *  By creating methods, we abstract unnecessary information from the user.
         *  eg: to convert a String into Uppercase, we use toUpperCase() method
         *
         * 3. Inheritance - when two classes have parent-child relationship.
         *  to establish, we use "extends" keyword with child class
         *  any class can extend ONLY 1 class (why? - Diamond Problem)
         *
         *
         * 4. Polymorphism - one name but many forms
         *  1. Run Time Polymorphism
         *      Dynamic Binding , Method Overriding
         *
         *      This can be achieved only in Inheritance set-up.
         *      When we have methods with same signature in parent-class as child-class
         *      then we achieve Run Time Polymorphism.
         *
         * 2. Compile Time Polymorphism
         *      Static Binding , Method Overloading
         *
         *      In a class, when we have two or more methods with same name BUT different arguments/parameters
         *      Ways to set different arguments:
         *          1. different no. of arguments
         *          2. different type of arguments
         *          3. different sequence of arguments
         *
         * Note: Return type DO NOT play any role in Compile Time Polymorphism
         */
    }
}
